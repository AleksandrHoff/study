let c: Double = 4 // задаем значение с

D = b * b - 4 * a * c // извлекаем дискриминант

if D > 0 { //задаем условие при котром дискриминант больше нуля дает нам два корня
    x1 = (-b + sqrt(D)) / (2 * a) //извлекаем первый корень
