//3. *Пользователь вводит сумму вклада в банк и годовой процент. Найти сумму вклада через 5 лет.

typealias Ruble = Double //валюта
typealias Percent = Double // процент
var AmountOfDeposit : Ruble = 2000 // вводим сумму вклада
var MeterYears = 1 // говорим, что шагом измерения будет один год
let AnnualPercentage : Percent = 10.0 // устанавливаем годовой процент
while MeterYears <= 5 { // говорим, что нас интересует процент за пять лет
    let OnePercent = AmountOfDeposit / 100.0 //здесь мы находим чему равен один процент вклада
    let AnnualIncome = OnePercent * AnnualPercentage
    AmountOfDeposit = AmountOfDeposit + AnnualIncome // здесь мы находим сумму вклада через год
    MeterYears += 1
}
print("Сумма вклада через 5 лет равна \(AmountOfDeposit) рублей")
